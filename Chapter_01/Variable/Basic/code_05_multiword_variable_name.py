'''
 * variable naming conventions
    Different ways to create multi-word variable names in Python, 
   
     >> commonly use there case 
       -> 1. camel Case variable
       -> 2. Pascal Case Variable
       -> 3. snake case variable

'''
print("Camel Case variable")
print("Each word (except the first) starts with an uppercase letter, and there are no spaces or underscores.")

# variable declare

myNameIs = "KzRaihan"
fixPrice = 1050
varFlaot = 99.99

print("myNameIs : ",myNameIs)
print("fixPrice : ",fixPrice)
print("varFlaot : ",varFlaot)

print("Pascal Case variable")
print("Each word starts with an uppercase letter, including the first word.")

