"""
 String is data type that stores a sequence of characters
"""
# 1st way to express (notation): for a single(') quotation
str1 = 'My name is Kz'
print(str1)

# 2nd way to express (notation): for a double("") quotation
str2 = "I am 23 Years old."
print(str2)

# 3th way to express (notation): for a triple("""")  quotation
str3 = """I am form Bangladesh"""
print(str3)