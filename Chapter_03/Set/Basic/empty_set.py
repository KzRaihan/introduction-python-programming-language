""" 
Topic: set in python
obj  : create an empty set and 

syntax: 
     set_name = set()
"""
# create an empty set
null_set = set()
print(null_set)
print("Type: ", type(null_set))
print("length: ", len(null_set))

#  store same element in null_set : ->> set is mutable(changeable)
# first null_set is null or empty there is no element then store some elements within null_set
null_set = {"Kz", 4039, "CSE", 96.56}
print(null_set)
print("Length: ", len(null_set))