# type() function in python: 
    
    # returns the type of an object(data)
    # syntax: type(object_name or variable name)

print("\nExample 1: integer type")
num1 = 97
num2 = 50

print("Type of num1: ", type(num1)) # o/p: <class 'int'>
print("Type of num2: ", type(num2)) # o/p: <class 'int'>

print("\nExample 2: floating type")
num3 = 99.99

print("Type of num3 : ",type(num3)) # o/p: <class 'float'>

print("\nExample 3: string type")
txt = "Hello there"
txt2 = '40'

print("Type of txt: ", type(txt)) # o/p: <class'str'>
print("Type of txt2: ", type(txt2)) # o/p: <class'str'>


print("\nExample 4: boolean type")
is_true = True
print("Type of is_true: ", type(is_true)) # o/p: <class 'bool


print("\nExample 5: None type: ")

null_value = None

print("Type of null_value: ", type(null_value)) # o/p: <class 'NoneType'>
