# Data type: what kind of values that variable can hold.

# classification of Data type: Primarily data types in python
#   -> integer
#   -> float point numbers
#   -> strings
#   -> Booleans
#   -> None Data type

print("\ninteger data type: ")  # whole values

var1 = 10
var2 = 40
                            # here, var1, var2 , sum -> integer type variable
sum = var1 + var2
print(sum)

print("\nFloat point numbers data type: ") # decimal values

num1 = 6.023 
price = 66.66
                            # here, num1, price -> float point numbers type variable
print("Number: ", num1)
print("Book prices: ", price)

print("\nStrings data type: ") # text type of valueso

str1 = 'KzRaihan'
text = "Hello Everyone"
                            # here, str1, text -> strings type variable
print("Name: ", str1)
print("Text: ", text)

print("\nBooleans data type: ")  # Return either 'True' or 'False'

is_true = True
is_leap_year = False
                            # here, is_true, is_leap_year -> Booleans type variable
print("value1: ", is_true)
print("value2: ", is_leap_year)

print("\nNone data type: ")  # None -> variable does not have a value(absence of value)

null = None                 # here, null is None type variable
print("Null : ", null)

